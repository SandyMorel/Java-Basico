    void mostrardatos(String buscar){
         
         buscar=txtBuscar.getText();
        
        DefaultTableModel modelo = new DefaultTableModel();
         modelo.addColumn("ID");
        modelo.addColumn("Cedula");
        modelo.addColumn("Nombre");
        modelo.addColumn("Telefono");
        modelo.addColumn("Email");
        modelo.addColumn("Direccion");
       
        Tabla2.setModel(modelo);

        
       
        
        if (sql.equals("")){
            sql="SELECT ID,Cedula,Nombre,Telefono,Email,DIreccion FROM clientes";
        }
        
       
        
        if(rbCedula.isSelected()){
            sql="SELECT * FROM clientes WHERE Cedula ='"+buscar+"'";
            
        }else if(rbNombre.isSelected()){
             sql="SELECT * FROM clientes WHERE Nombre ='"+buscar+"'";
        }
            
             if (buscar.equals("")){
             sql="SELECT ID,Cedula,Nombre,Telefono,Email,DIreccion FROM clientes";

        }
        
        String [] dato = new String [6];
        
        try {
           Statement st = cn.createStatement();
           ResultSet rs = st.executeQuery(sql);
           
           while(rs.next()){
               
               dato[0] = rs.getString(1);
               dato[1] = rs.getString(2);
               dato[2] = rs.getString(3);
               dato[3] = rs.getString(4);
               dato[4] = rs.getString(5);
               dato[5] = rs.getString(6);
               
               modelo.addRow(dato);
               Tabla2.setModel(modelo);
           }
      
            
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
         
        
    }

