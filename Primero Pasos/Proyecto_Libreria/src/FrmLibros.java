     void mostrardatos(String buscar){
         
         buscar=txtBtitulo.getText();
        
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Codigo");
        modelo.addColumn("Titulo");
        modelo.addColumn("Autor");
        modelo.addColumn("Editorial");
        modelo.addColumn("Paginas");
        modelo.addColumn("Stock");
        modelo.addColumn("Categorias");
        Tabla.setModel(modelo);

        
       
        
        if (sql.equals("")){
            sql="SELECT Codigo,Titulo,Autor,Editorial,Paginas,Stock,Categoria FROM libros";
        }else{
             sql="SELECT * FROM libros WHERE Titulo ='"+buscar+"'";

        }if (buscar.equals("")){
             sql="SELECT Codigo,Titulo,Autor,Editorial,Paginas,Stock,Categoria FROM libros";

        }
        
        String [] dato = new String [7];
        
        try {
           Statement st = cn.createStatement();
           ResultSet rs = st.executeQuery(sql);
           
           while(rs.next()){
               
               dato[0] = rs.getString(1);
               dato[1] = rs.getString(2);
               dato[2] = rs.getString(3);
               dato[3] = rs.getString(4);
               dato[4] = rs.getString(5);
               dato[5] = rs.getString(6);
               dato[6] = rs.getString(7);
               modelo.addRow(dato);
               Tabla.setModel(modelo);
           }
      
            
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
        
    }

