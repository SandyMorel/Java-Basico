    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:

        int fila = (Tabla3.getSelectedRow());
       String titulo = (String) Tabla3.getValueAt(fila,0);
        if (fila >=0){
            
            
             int ax = JOptionPane.showConfirmDialog(null, "Estas seguro que desea eliminar este libro?");
              if(ax == JOptionPane.YES_OPTION){
                JOptionPane.showMessageDialog(null, "EL Libro fue eliminado con Ã©xito!!!");
                 try {
                PreparedStatement pst = cn.prepareStatement("DELETE from libros where  Codigo = '"+titulo+"'");
                pst.executeUpdate();
                
            } catch (SQLException e) {
                System.out.println(e.getMessage());
            }

              
            }
            else if(ax == JOptionPane.NO_OPTION){
                JOptionPane.showMessageDialog(null, "No se elimino el libro");
            }
              mostrardatos("");

          

           

        }
        
           
           
    }//GEN-LAST:event_btnEliminarActionPerformed

