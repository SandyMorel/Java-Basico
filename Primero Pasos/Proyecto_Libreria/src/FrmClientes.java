    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
   
        
        try {
            PreparedStatement pst  = cn.prepareStatement("Update clientes SET  Nombre='"+txtNombre.getText()+"',Cedula='"+txtCedula.getText()+"',Telefono='"+txtTelefono.getText()+"',Email='"+txtEmail.getText()+"',Direccion='"+txtDireccion.getText()+"' WHERE Cedula='"+txtCedula.getText()+"'");        
         
 
            pst.executeUpdate();

            int ax = JOptionPane.showConfirmDialog(null, "Estas seguro de actualizar este Cliente?");
            if(ax == JOptionPane.YES_OPTION){
                JOptionPane.showMessageDialog(null, "EL Cliente fue actualizado con éxito!!!");

                txtNombre.setText("");
                txtCedula.setText("");
                txtTelefono.setText("");
                txtEmail.setText("");
                txtDireccion.setText("");
             

            }
            else if(ax == JOptionPane.NO_OPTION){
                JOptionPane.showMessageDialog(null, "No se actualizo el Cliente");
            }

        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
        
        dispose();
    }//GEN-LAST:event_btnActualizarActionPerformed

