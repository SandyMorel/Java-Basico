    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        
          try {
            PreparedStatement pst  = cn.prepareStatement("INSERT INTO clientes(Nombre,Cedula,Telefono,Email,Direccion)values(?,?,?,?,?)");
            pst.setString(1, txtNombre.getText());
            pst.setString(2, txtCedula.getText());
            pst.setString(3, txtTelefono.getText());
            pst.setString(4, txtEmail.getText());
            pst.setString(5, txtDireccion.getText());
            
             
            pst.executeUpdate();

            int ax = JOptionPane.showConfirmDialog(null, "Estas seguro de agregar este Cliente?");
            if(ax == JOptionPane.YES_OPTION){
                JOptionPane.showMessageDialog(null, "EL Cliente fue agregado con éxito!!!");

                txtNombre.setText("");
                txtCedula.setText("");
                txtTelefono.setText("");
                txtEmail.setText("");
                txtDireccion.setText("");
             

            }
            else if(ax == JOptionPane.NO_OPTION){
                JOptionPane.showMessageDialog(null, "No se agrego el Cliente");
            }

        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

